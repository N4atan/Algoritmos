//Essas comparações retornam booleans, no caso true ou false.

const x = 17
const y = 17

//"===" igual
console.log(x === (2*25)) //False
console.log(x === 2007) //False
console.log(x === y) //True

//"!==" diferente
console.log(x !== y) //false

//">" maior
console.log(x > y) //False

//">=" maior ou igual
console.log(x >= y) //True

//"<" menor
console.log(x < y) //False

//"<=" menor ou igual
console.log(x <= y) //True

